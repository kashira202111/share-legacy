  end

  # POST /items
  # POST /items.json
  def create
    require 'aws-sdk'
    @item = Item.new(item_params)
    s3 = Aws::S3::Resource.new(region: 'ap-northeast-1', access_key_id: "", secret_access_key: "")
    bucket = 'test-share-legacy'
    prename = @item.image
    name = "img/" + "test.png" 
    output_path="/tmp/test.png"

    File.open(output_path, 'w+b') do |fp|
      fp.write params[:item][:image].read
    end

    obj = s3.bucket(bucket).object(name)
    obj.upload_file(output_path)
    respond_to do |format|
      if @item.save
        format.html { redirect_to @item, notice: 'Item was successfully created.' }
        format.json { render :show, status: :created, location: @item }
      else
        format.html { render :new }
        format.json { render json: @item.errors, status: :unprocessable_entity }
      end
    end
  end

  # PATCH/PUT /items/1
  # PATCH/PUT /items/1.json
  def update
    respond_to do |format|
      if @item.update(item_params)
        format.html { redirect_to @item, notice: 'Item was successfully updated.' }
        format.json { render :show, status: :ok, location: @item }
      else
        format.html { render :edit }
        format.json { render json: @item.errors, status: :unprocessable_entity }
      end
    end
  end  

  # DELETE /items/1
  # DELETE /items/1.json
  def destroy
    @item.destroy
    respond_to do |format|
      format.html { redirect_to items_url, notice: 'Item was successfully destroyed.' }
      format.json { head :no_content }
    end
  end

  private
    # Use callbacks to share common setup or constraints between actions.
    def set_item
      @item = Item.find(params[:id])
    end

    # Never trust parameters from the scary internet, only allow the white list through.
    def item_params
      params.require(:item).permit(:photo_file_name, :photo_content_type, :photo_file_size, :photo_updated_at, :image)
    end
end
